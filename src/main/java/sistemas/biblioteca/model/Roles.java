package sistemas.biblioteca.model;

public class Roles {

    private long id;
    private String rol;

    public Roles() {}

    public long getId() {
        return id;
    }

    public String getRol() {
        return rol;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

}
